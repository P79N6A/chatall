import request from '@/utils/request'

const changeInfo = {
    subInfo(data) {
        return request({
            url: '/userInfo/editUserInfo',
            method: 'post',
            data
        })
    },
    subPwd(data) {
        return request({
            url: '/userInfo/updatePassword',
            method: 'post',
            data
        })
    }
}

export default changeInfo

